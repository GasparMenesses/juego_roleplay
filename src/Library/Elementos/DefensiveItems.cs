namespace Library.Elementos;

public class DefensiveItems
{
    public static void Shield(Character name)
    {
        name.Defensa += 5;
    }

    public static void Armor(Character name)
    {
        name.Defensa += 5;
    }
    public static void Helmet(Character name)
    {
        name.Defensa += 3;
    }
}